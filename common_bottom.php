    document.querySelector('.lang .dropdown_btn').addEventListener('click', function() {
        const langdropdownContent = document.querySelector('.lang .dropdown_content');
        const icon = this.querySelector('i');
        if (langdropdownContent.style.display === 'block') {
            langdropdownContent.style.display = 'none';
            icon.classList.remove('bi-chevron-up');
            icon.classList.add('bi-chevron-down');
        } else {
            langdropdownContent.style.display = 'block';
            icon.classList.remove('bi-chevron-down');
            icon.classList.add('bi-chevron-up');
        }
    });

    // Close the dropdown
    window.onclick = function(event) {
        if (!event.target.matches('.lang .dropdown_btn') && !event.target.matches('.lang .dropdown_btn i')) {
            const langdropdowns = document.getElementsByClassName('lang_dropdown_content');
            const icons = document.querySelectorAll('.lang .dropdown_btn i');
            for (let i = 0; i < langdropdowns.length; i++) {
                const langopenDropdown = langdropdowns[i];
